// Team Super Bowl history helpers
export function getTeamSuperBowlHistory(teamId: string) {
  const appearances = superBowlHistory.filter(
    (sb) => sb.winnerTeamId === teamId || sb.loserTeamId === teamId
  );
  const wins = appearances.filter((sb) => sb.winnerTeamId === teamId);
  const losses = appearances.filter((sb) => sb.loserTeamId === teamId);

  return {
    appearances: appearances.length,
    wins: wins.length,
    losses: losses.length,
    games: appearances.map((sb) => ({
      ...sb,
      result: sb.winnerTeamId === teamId ? 'W' : 'L',
    })),
  };
}

// Get all Super Bowl wins by team
export function getSuperBowlWinsByTeam() {
  const winsByTeam: Record<string, number> = {};

  superBowlHistory.forEach((sb) => {
    if (!winsByTeam[sb.winnerTeamId]) {
      winsByTeam[sb.winnerTeamId] = 0;
    }
    winsByTeam[sb.winnerTeamId]++;
  });

  return Object.entries(winsByTeam)
    .sort((a, b) => b[1] - a[1])
    .map(([teamId, wins]) => ({ teamId, wins }));
}
